import SwiftUI

extension Font {
    static func abrilFatfaceRegular(size: CGFloat) -> Font {
        .custom(Fonts.abrilFatfaceRegular, size: size)
    }
    static func firaSansExtraBold(size: CGFloat) -> Font {
        .custom(Fonts.firaSansExtraBold, size: size)
    }
    static func oswaldVariableFontWght(size: CGFloat) -> Font {
        .custom(Fonts.oswaldVariableFontWght, size: size)
    }
}

